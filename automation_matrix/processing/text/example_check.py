def insert_breaks(input_file_path, output_file_path):
    chapter_pattern = re.compile(r'^\d+(\.\d+)*[a-d]?')
    ignore_start_pattern = re.compile(r'^={7,}')
    ignore_end_pattern = re.compile(r'^-{7,}')
    ignore_section = False

    with open(input_file_path, 'r') as input_file, open(output_file_path, 'w') as output_file:
        for line in input_file:
            if ignore_section:
                output_file.write(line)
                if ignore_end_pattern.match(line):
                    ignore_section = False
            else:
                if ignore_start_pattern.match(line):
                    ignore_section = True
                    output_file.write(line)
                else:
                    stripped_line = line.lstrip()
                    if chapter_pattern.match(stripped_line):
                        output_file.write("--break--\n")
                    output_file.write(line)


